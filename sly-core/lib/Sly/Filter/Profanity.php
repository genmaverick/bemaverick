     * Defined by Zend_Filter_Interface
     *
     * Returns the string $value, stripping the chars
     *
     * @param  string $value
     * @return string
     */
    public function filter($value)
    {
        return preg_replace_callback( $this->profanity,
                                      array('Sly_Filter_Profanity', 'star'),
                                      (string) $value );
    }

    public static function star($matches)
    {
        preg_match('/[A-Za-z0-9]/', strrev($matches[0]), $alphanumMatches, PREG_OFFSET_CAPTURE);

        return str_pad('', strlen($matches[0]) - @$alphanumMatches[0][1], '*') .
                           substr($matches[0], strlen($matches[0]) - @$alphanumMatches[0][1]);
    }
